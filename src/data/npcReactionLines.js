export function getNpcReactionLine(npcId, type, count = 0) {
  const lines = npcReactionLines[npcId]?.[type] || [];
  if (!lines.length) return type === 'win' ? '这题我拿下。' : '这题算你稳。';
  return lines[Math.max(0, count - 1) % lines.length];
}
